  handleCollision(obj) {
    const collisionTiles = this.findCollisionTiles(obj);
    // return arr of tuples containing tile coords ([row, col])
    if (collisionTiles) {
      collisionTiles.forEach(tileTuple => {
        this.resolveCollision(obj, ...tileTuple);
      });
    }
  }

  // return arr of tiles to check
  findCollisionTiles(obj) {
    return [
      this.convertPosToTile(obj.movement.posX, obj.movement.posY),
      this.convertPosToTile(obj.movement.posX + obj.width, obj.movement.posY),
      this.convertPosToTile(obj.movement.posX, obj.movement.posY + obj.height),
      this.convertPosToTile(obj.movement.posX + obj.width, obj.movement.posY + obj.height),
    ];
  }

  convertPosToTile(posX, posY) {
    const row = Math.floor(posY / this.size);
    const col = Math.floor(posX / this.size);
    return [row, col];
  }

  // currently returns collision val (int)
  getCollisionTile(row, col) {
    // debugger
    return this.collisionMap[row][col];
  }

  resolveCollision(obj, row, col) {
    const collisionValue = this.getCollisionTile(row, col);
    
    let didCollide;
    switch (collisionValue) {
      case 1:
        didCollide = this.checkTop(obj, row);
        break;

      case 2:
        didCollide = this.checkRight(obj, col);
        break;

      case 3:
        didCollide = this.checkBottom(obj, row);
        break;
      
      case 4:
        didCollide = this.checkLeft(obj, col);
        break;

      default:
        didCollide = false;
        break;
    }
    
    if (didCollide && obj instanceof Bullet) {
      obj.movement.velX = 0;
      obj.movement.velY = 0;
    }
  }

  checkTop(obj, row) {
    const tileY = row * this.size;
    if ((obj.movement.posY + obj.height) > tileY) {
      obj.movement.posY = tileY - obj.height;
      obj.movement.velY = 0;
      return true;
    }
    return false;
  }
  
  checkRight(obj, col) {
    const tileX = (col + 1) * this.size;
    if (obj.movement.posX < tileX) {
      obj.movement.posX = tileX;
      obj.movement.velX = 0;
      return true;
    }
    return false;
  }
  
  checkBottom(obj, row) {
    const tileY = (row + 1) * this.size;
    if (obj.movement.posY < tileY) {
      obj.movement.posY = tileY;
      obj.movement.velY = 0;
      return true;
    }
    return false;
  }
    
  checkLeft(obj, col) {
    const tileX = col * this.size;
    if ((obj.movement.posX + obj.width) > tileX) {
      obj.movement.posX = tileX - obj.width;
      obj.movement.velX = 0;
      return true;
    }
    return false;
  }
